export type Language = 'en' | 'te';

export const useTranslation = (language: Language) => {
  return (key: string): string => {
    const keys = key.split('.');
    let value: any = translations[language];
    
    for (const k of keys) {
      value = value?.[k];
    }
    
    return value || key;
  };
};
